struct mutex globalLock;
__thread aeEventLoop *eventLoopThread = NULL;

struct aeAsyncTask {
    int lock;
    aeAsyncProc* proc;
    void *var;
    aeAsyncCallback *callback;
};

/* This function reads an asynchronous task command from a specified file descriptor
 * and executes the corresponding task processing function. */
static void aeAsyncTaskProcess(aeEventLoop *eventLoop, int fd, void *clientData, int mask) {
    AE_NOTUSED(eventLoop);
    AE_NOTUSED(clientData);
    AE_NOTUSED(mask);

    /* Defines a deferred lock mutex to ensure thread-safe access
     * to global resources. */
    WRAPPER_MUTEX_DEFER_LOCK(wm, &globalLock);
   
    struct aeAsyncTask cmd;
    ssize_t cb = read(fd, &cmd, sizeof(struct aeAsyncTask));
    if (cb != sizeof(cmd)) {
        assert(errno == EAGAIN);
        return;
    }

    /* Attempts to acquire the lock if the command requires it
     * and the current thread does not own it. */
    if (cmd.lock && !wrapperMutexOwnLock(&wm))
        wrapperMutexLock(&wm);
    cmd.proc(cmd.var, cmd.callback);
}

/* This function either directly invokes the specified asynchronous function if
 * called from the event loop thread, or sends a command to the event loop thread
 * for execution. It's designed to facilitate non-blocking operations. */
int aeAsyncFunction(aeEventLoop *eventLoop, aeAsyncProc *proc, void *var, aeAsyncCallback *callback, int lock) {
    if (eventLoop == eventLoopThread) {
        proc(var, callback);
        return AE_OK;
    }

    struct aeAsyncTask cmd;
    cmd.proc = proc;
    cmd.lock = lock;
    cmd.var = var;
    cmd.callback = callback;

    ssize_t size = write(eventLoop->fdAsyncWrite, &cmd, sizeof(cmd));
    if (!eventLoop->stop) {
        if (!(!size || size == sizeof(cmd))) {
            printf("aeAsyncFunction error: %s\n", strerror(errno));
        }
        assert(!size || size == sizeof(cmd));
    }

    return size > 0 ? AE_OK : AE_ERR;
}

